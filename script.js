function kurt() {
    let name= prompt("¿Quién es este artista?")
    let nombre = document.getElementById("nombre")
    nombre.textContent = name

    let genero= prompt("¿A qué género musical pertenece?")
    let musica = document.getElementById("musica")
    musica.textContent = genero

    let country= prompt("¿De qué país era?")
    let pais = document.getElementById("pais")
    pais.textContent = country

    let cancion= prompt("Ingresa tu canción favorita")
    let song = document.getElementById("song")
    song.textContent = cancion
}
